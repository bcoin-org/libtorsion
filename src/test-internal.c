static int
get_entropy(void *dst, size_t len) {
#ifndef _WIN32
  char *ptr = (char *)dst;
  size_t left = len;
  int fd = open("/dev/urandom", O_RDONLY | O_CLOEXEC);

  if (fd == -1) {
    fd = open("/dev/random", O_RDONLY | O_CLOEXEC);
    if (fd == -1)
      return 0;
  }

  while (left > 0) {
    int bytes = read(fd, ptr, left);

    if (bytes <= 0) {
      close(fd);
      return 0;
    }

    assert((size_t)bytes <= left);

    left -= bytes;
    ptr += bytes;
  }

  assert(left == 0);
  assert(ptr == (char *)dst + len);

  close(fd);

  return 1;
#else
  return 0;
#endif
}

static void
random_init(drbg_t *rng) {
  unsigned char entropy[ENTROPY_SIZE];

  if (!get_entropy(entropy, ENTROPY_SIZE)) {
    size_t i;

    for (i = 0; i < ENTROPY_SIZE; i++)
      entropy[i] = (unsigned char)rand();
  }

  drbg_init(rng, HASH_SHA256, entropy, ENTROPY_SIZE);
}

static void
random_bytes(drbg_t *rng, void *dst, size_t len) {
  drbg_generate(rng, dst, len);
}

static unsigned int
random_int(drbg_t *rng, unsigned int mod) {
  unsigned int x;

  if (mod == 0)
    return 0;

  random_bytes(rng, &x, sizeof(x));

  return x % mod;
}

static void
print_hex(const unsigned char *data, size_t len) {
  char str[512 + 1];
  size_t i;

  memset(str, 0, sizeof(str));

  assert(len <= 256);

  for (i = 0; i < len; i++) {
    char hi = data[i] >> 4;
    char lo = data[i] & 0x0f;

    if (hi < 10)
      hi += '0';
    else
      hi += 'a' - 10;

    if (lo < 10)
      lo += '0';
    else
      lo += 'a' - 10;

    str[i * 2 + 0] = hi;
    str[i * 2 + 1] = lo;
  }

  str[i * 2] = '\0';

  printf("%s\n", str);
}

static size_t
stupid_bit_length(uint32_t x) {
  size_t bits = 0;

  while (x != 0) {
    bits += 1;
    x >>= 1;
  }

  return bits;
}

static int
stupid_bytes_zero(const unsigned char *a, size_t size) {
  while (size--) {
    if (a[size] != 0)
      return 0;
  }

  return 1;
}

static int
revcmp(const unsigned char *a, const unsigned char *b, size_t size) {
  while (size--) {
    if (a[size] < b[size])
      return -1;

    if (a[size] > b[size])
      return 1;
  }

  return 0;
}

